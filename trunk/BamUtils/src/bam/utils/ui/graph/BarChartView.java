    public BarChartView() {
        initComponents();
    }
    
    public void setModel(BarChartModel model){
        removeAll();
        setLayout(new BorderLayout());
        chartPanel = new ChartPanel(createChart(model));
        add(chartPanel);
        model.addListener(this);
    }

    private JFreeChart createChart(BarChartModel model) {
        jfreechart = ChartFactory.createBarChart(model.getChartTitle(), model.getxAxeTitle(), model.getyAxeTitle(), model.getChartModel(), PlotOrientation.VERTICAL, false, true, false);
        jfreechart.setBackgroundPaint(Color.white);
        CategoryPlot categoryplot = (CategoryPlot) jfreechart.getPlot();
        categoryplot.setBackgroundPaint(Color.white);
        
        categoryplot.setDomainGridlinePaint(new Color(200, 200, 200));
        categoryplot.setDomainGridlinesVisible(true);
        categoryplot.setRangeGridlinePaint(new Color(200, 200, 200));
        categoryplot.setRangeGridlinesVisible(true);
        
        //NumberAxis numberaxis = (NumberAxis) categoryplot.getRangeAxis();
        //numberaxis.setStandardTickUnits(NumberAxis.createIntegerTickUnits());
        barrenderer = (BarRenderer) categoryplot.getRenderer();
        barrenderer.setDrawBarOutline(true);
        barrenderer.setBarPainter(new StandardBarPainter());
        //CategoryAxis categoryaxis = categoryplot.getDomainAxis();
        //categoryaxis.setCategoryLabelPositions(CategoryLabelPositions.createUpRotationLabelPositions(0.52359877559829882D));
        
        CategoryAxis domainAxis = categoryplot.getDomainAxis();
        domainAxis.setCategoryLabelPositions(CategoryLabelPositions.UP_45);
        domainAxis.setTickMarksVisible(true);
        
        for (String bar : model.getBars()) onBarAdded(model, bar);
        
        return jfreechart;
    }

