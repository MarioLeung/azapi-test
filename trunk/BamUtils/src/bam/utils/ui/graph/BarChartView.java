    @Override
    public void onBarAdded(BarChartModel source, String barName) {
        final int rowIndex = source.getChartModel().getColumnIndex(barName);
        barrenderer.setSeriesItemLabelsVisible(rowIndex, true);
        barrenderer.setSeriesPaint(rowIndex, new Color(160,200,255));
    }
