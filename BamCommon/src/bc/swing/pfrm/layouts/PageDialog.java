    public static void showClean(bc.swing.pfrm.Page p) {
        JFrame nframe = null;
        final JDialog diag = new JDialog(nframe, p.getName(), true);
        diag.setContentPane(p.getView());

        diag.pack();
        diag.setLocationRelativeTo(null);
        diag.setResizable(true);

        p.addToDisposeList(new PageView() {

            public void setPage(Page model) {
                //
            }

            public void onDispose() {
                diag.setVisible(false);
            }
        });


        diag.setVisible(true);
    }

    public static void showClean(bc.swing.pfrm.Page p, int height, int width) {
        JFrame nframe = null;
        final JDialog diag = new JDialog(nframe, p.getName(), true);
        diag.setContentPane(p.getView());

        diag.setPreferredSize(new Dimension(width, height));
        diag.pack();
        diag.setLocationRelativeTo(null);
        diag.setResizable(true);


        p.addToDisposeList(new PageView() {

            public void setPage(Page model) {
                //
            }

            public void onDispose() {
                diag.setVisible(false);
            }
        });

        diag.setVisible(true);
    }

